#include "../includes/fractol.h"

unsigned int	get_color(int n)
{
	int	r;
	int	g;
	int	b;

	if (n == MAX_ITER)
		return (0x000000);
	r = (n % 256);
	g = ((n * 2) % 256);
	b = ((n * 3) % 256);
	return ((unsigned int)(r << 16) | (g << 8) | b);
}
